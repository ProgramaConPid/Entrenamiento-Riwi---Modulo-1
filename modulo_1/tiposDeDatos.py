# Tipos de datos

# STR -> Cadenas de texto
cadenaDeTexto = str("Hola mundo, soy felipe")
print(cadenaDeTexto)

# INT -> numeros enteros
numeroEntero = int(34)
print(numeroEntero)

# FLOAT -> Numeros flotantes (decimales)
numeroDecimal = float(34.78)
print(numeroDecimal)

# BOOL -> Booleanos (True o False)
booleano = bool()
print(booleano)

# TYPE -> Verificar el tipo de dato de una variable
print(type(numeroEntero))

#.__name__ -> Devuelve unicamente el tipo de dato de la variable en string
print(type(numeroDecimal).__name__)

